    REQUEST_INVALID = "REQUEST_INVALID"
    UNKNOWN_AUTHORITY_FIELD = "UNKNOWN_AUTHORITY_FIELD"
    ARBITRARY_CALL_REFUSED = "ARBITRARY_CALL_REFUSED"
    ACTION_NOT_ALLOWED = "ACTION_NOT_ALLOWED"
    NETWORK_NOT_ALLOWED = "NETWORK_NOT_ALLOWED"
    ASSET_NOT_ALLOWED = "ASSET_NOT_ALLOWED"
    CONTRACT_NOT_ALLOWED = "CONTRACT_NOT_ALLOWED"
    METHOD_NOT_ALLOWED = "METHOD_NOT_ALLOWED"
    MODULE_NOT_ALLOWED = "MODULE_NOT_ALLOWED"
    AMOUNT_INVALID = "AMOUNT_INVALID"
    AMOUNT_LIMIT_EXCEEDED = "AMOUNT_LIMIT_EXCEEDED"
    MAX_FEE_REQUIRED = "MAX_FEE_REQUIRED"
    MAX_FEE_EXCEEDED = "MAX_FEE_EXCEEDED"
    POLICY_VERSION_MISMATCH = "POLICY_VERSION_MISMATCH"
    POLICY_AUTHORITY_DISABLED = "POLICY_AUTHORITY_DISABLED"
    ACTION_ID_INVALID = "ACTION_ID_INVALID"
    ACTION_ALREADY_ACTIVE = "ACTION_ALREADY_ACTIVE"
    ACTION_MUTATED = "ACTION_MUTATED"
    ACTION_REPLAYED = "ACTION_REPLAYED"


